<h3 class="underline">GLANCE FOR SALESFORCE</h3>

<h4>THE CLIENT</h4>

<p>Glance enables sales and support reps to launch demos, presentations, and support sessions directly or from within applications they’re already using like Salesforce.com.</p>

<h4>THE TEST</h4>

<p>Glance Networks wanted a simple, yet powerful technological edge to help sales pros make a big impact on every call throughout the sales cycle. They needed us to provide a management method to track Sales and Support Glance sessions, plus integrate Glance session data with Salesforce.com.</p>

<h4>THE SCIENCE</h4>

<p>We created a lightweight composite app that integrates with native page layouts, seamlessly launches the desktop app, and integrates with Glance API and Chatter.</p>

<h4>THE RESULTS</h4>

<p>Glance now gives users the ability share their screen or view a client’s screen instantly from their record in Salesforce.com. The user can also schedule future sessions from Accounts, Contacts, Leads, Cases & Campaigns; relate Glance Events with CRM objects and pull data from Glance servers; and serve up Glance sessions in the Chatter Feed to keep the company up to the minute on Sales and Support.</p>

